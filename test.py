def add(x: int, y: int) -> int:
    """Add two integers

    Args:
        x (int): x is an integer
        y (int): y is an integer

    Returns:
        int: the sum of two integers
    """
    # x is an integer
    # y is an integer
    return x + y

print(add(3, 2))

print(add('string1', 'string2'))